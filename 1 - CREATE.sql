-- Crear tabla de empleados
CREATE TABLE Empleados (
    Codigo int, 
    Nombre varchar(50), 
    Apellido varchar(50), 
    Edad int, 
    Genero varchar(50)
)

-- Crear tabla de salarios
CREATE TABLE SalarioEmpleados (
    Codigo int,
    Posicion varchar(50), 
    Salario int
)



